export class User {
    constructor({ id, username, password, name, lastname, email, register_date, tuition }) {
        this.id = id;
        this.username = username;
        this.password = password;
        this.name = name;
        this.lastname = lastname;
        this.email = email;
        this.register_date = register_date;
        this.tuition = tuition;

    }
}